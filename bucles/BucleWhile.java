public class BucleWhile {

    public static void main(String[] args) {
        int multiplo = 1;
        int indice = 1;
        System.out.println("bucle while");
        while (multiplo <= 10) {

            System.out.println(multiplo + " X " + indice + " = " + multiplo * indice);
            multiplo++;
            indice++;

        }
    }
}
